	VOID
dos_sint(interupt, ad_code)
	WORD		interupt;
	LONG		ad_code;
{
	DOS_AX = 0x2500 | interupt;
	DOS_DX = LLOWD(ad_code);
	DOS_DS = LHIWD(ad_code);

	__DOS();

} /* dos_sint */
